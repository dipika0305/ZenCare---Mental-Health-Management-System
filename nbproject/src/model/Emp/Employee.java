package model.Emp;

/**
 *
 * @author dipikam
 */
public class Employee {
    
    int eid;
    String name;
    static int count = 1;

    public Employee() {
        eid = count;
        count++;
    }

    public int getEid() {
        return eid;
    }

    public void setEid(int eid) {
        this.eid = eid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
    
}
