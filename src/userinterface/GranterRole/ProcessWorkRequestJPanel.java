    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        if(message.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Do not enter a empty  message!!");
            return;
            
        }
        else if(message.getText().length()>25)
        {
            JOptionPane.showMessageDialog(null,"DO not exceed 25 characters limit !!");
            return;
            
        }else if(approveBox.isSelected() && rejectedBox.isSelected()){
            JOptionPane.showMessageDialog(null,"please select only 1 check box!!");
            return;
            
        }
        else if(approveBox.isSelected()==false && rejectedBox.isSelected()==false){
            JOptionPane.showMessageDialog(null,"please select  1 check box!!");
            return;
            
        }else {
            
                try{

                    Integer.parseInt(grantedAmount.getText());
                }catch(Exception ex){
                         JOptionPane.showMessageDialog(null,"Enter a valid Amount");
                             return;
                }
                
            request.setGrantingAmount((grantedAmount.getText()));
            
            JOptionPane.showMessageDialog(null,"Completed");     
            
            if(approveBox.isSelected()){
                
                if(Integer.parseInt(grantedAmount.getText())<=0 && Integer.parseInt(grantedAmount.getText())>=Integer.parseInt(request.getBill())){
                            JOptionPane.showMessageDialog(null,"Enter a valid Amount");
                             return;
                }
                
                
            request.setGrantingstatus("Approved");
                this.status="Claim Approved";
            }if(rejectedBox.isSelected()){
                this.status="Claim Rejected";
                request.setGrantingstatus("Rejected");
            }
            
            BillingWorkRequest newrequest = new BillingWorkRequest();
        
            newrequest.setMessage(message.getText());
            newrequest.setSender(account);
            newrequest.setClaimstatus(status); ///add condition
            newrequest.setType("Patient");
            newrequest.setPatient(request.getPatient());
            newrequest.setForwardrequest(status);
            
            int temp=Integer.parseInt(request.getBill());
            
            try{
                
             temp = Integer.parseInt(request.getBill()) - Integer.parseInt(grantedAmount.getText());
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "");
            }
            
            this.amount=String.valueOf(temp);
            newrequest.setBill(String.valueOf(temp));
            

            Organization org = null;
            for (Organization organization : business.getNetworkList().get(0).getEnterpriseDirectory().getEnterpriseList().get(1).getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof OrgBillings) {
                    org = organization;
                    break;
                }
            }
            if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(newrequest);
                //userAccount.getWorkQueue().getWorkRequestList().add(request);
            }
            
        
             try {
                sendemailto();
            } catch (Exception ex) {
                Logger.getLogger(BillGeneratorWorkAreaJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            JOptionPane.showMessageDialog(null, "Request sent to Govt for funds ");
            
            ///can add email
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

    
